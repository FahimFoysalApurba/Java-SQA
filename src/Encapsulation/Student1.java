package Encapsulation;

public class Student1 {
    // Private data members
    private String name;
    private int age;

    // Setter method to set the value of 'name' (only write, no getter)
    public void setName(String name) {
        this.name = name;
    }

    // Setter method to set the value of 'age' (only write, no getter)
    public void setAge(int age) {
        this.age = age;
    }
}
